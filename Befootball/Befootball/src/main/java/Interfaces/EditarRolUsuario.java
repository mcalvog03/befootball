    // Crear factorty
    private void initializeSessionFactory() {
        try {
            factory = new Configuration().configure().buildSessionFactory();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al inicializar Hibernate: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    // Metodo para seleccionar el equipo favorito del usuario utilizando
    public void editarRol(int pkUsuario, String rol) {
        Transaction tx = null;
        try (Session session = factory.openSession()) {
            tx = session.beginTransaction();

            // Obtener el usuario por id
            Usuarios usuario = session.find(Usuarios.class, pkUsuario);

            if (usuario != null) {
                // Establecer el rol seleccionado en el usuario
                usuario.setRol(rol);

                // Guardar los cambios en la base de datos
                session.persist(usuario);

                tx.commit();
                JOptionPane.showMessageDialog(null, "Rol actualizado a " + rol + " para " + pkUsuario, "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                System.out.println("Usuario no encontrado con ID: " + pkUsuario);
                JOptionPane.showMessageDialog(null, "Usuario no encontrado con ID: " + pkUsuario, "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            if (tx != null) {
                tx.rollback();
            }
            e.printStackTrace();
        }
    }

